void buffer_check(void)
{
	uint8_t c;
	static uint8_t i = 0;
	
	if(!ring_buffer_is_empty(&data_buffer))
	{
		c = ring_buffer_get(&data_buffer);
		if(0  == start)
		{
			if(c=='*')
			{
				start = 1;
				i = 0;
			}
		} else if(1  == start)
		{
			if(c=='#')
			{
				start = 2;
			}
			else
			{
				data_command[i++]=c;
			}
		}
	}
	
	if(2  == start)
	{
		//compare and control led
		if(strcmp(data_command,"led on")==0)
		{
			HAL_GPIO_WritePin(GPIOC,GPIO_PIN_8,1);
		}else if(strcmp(data_command,"led off")==0)
		{
			HAL_GPIO_WritePin(GPIOC,GPIO_PIN_8,0);
		}
		memset(data_command,0,20);
		start = 0;
	}
	
}
